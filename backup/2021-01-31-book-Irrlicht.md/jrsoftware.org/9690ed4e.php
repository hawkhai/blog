<p>Inno Setup is a <i>free</i> installer for Windows programs by Jordan Russell and Martijn Laan. First introduced in 1997, Inno Setup today rivals and even surpasses many commercial installers in feature set and stability.</p>

<ul class="linespace">
  <li><span class="big"><a href="#features">Features</a></span><br />Learn more about what Inno Setup can do.</li>
  <li><span class="big"><a href="/isdl.php">Download Inno Setup</a></span><br />Get the latest version of Inno Setup here.</li>
  <li><span class="big"><a href="/ismail.php">Mailing List</a></span><br />Be notified by e-mail whenever major new versions are released.</li>
  <li><span class="big"><a href="/isfaq.php">Frequently Asked Questions</a></span><br />Get answers to common questions and problems.</li>
  <li><span class="big"><a href="/is3rdparty.php">Third-Party Files</a></span><br />Here you can find various useful tools for Inno Setup (for example, GUI script editors) and translations of the Setup text in many languages.</li>
  <li><span class="big"><a href="/isdonate.php">Donate</a></span><br />Support the Inno Setup project.</li>
</ul>

<p>Don't forget to check out the Inno Setup <a href="/forums.php">forum</a>, the primary source for Inno Setup support. Additionally, a large number of questions and answers can be found on <a href="http://stackoverflow.com/questions/tagged/inno-setup/">Stack Overflow</a>.</p>

<p><b>Want to be notified by e-mail of updates?</b> Then <a href="ismail.php">click here to subscribe</a> to the Inno Setup announcements mailing list. <b>If you subscribed before October 2019, please resubscribe.</b></p>

<p><a name="features"><b><i>Key features:</i></b></a></p>

<ul>
  <li>Support for every Windows release since 2006, including: Windows 10, Windows 10 on ARM, Windows Server 2019, Windows Server 2016, Windows 8.1, Windows 8, Windows Server 2012, Windows 7, Windows Server 2008 R2, Windows Server 2008, and Windows Vista. (No service packs are required.)</li>
  <li>Extensive support for installation of <b>64-bit applications</b> on the 64-bit editions of Windows. The x64, ARM64 and Itanium architectures are all supported.</li>
  <li>Extensive support for both administrative and non administrative installations.</li>
  <li>Supports creation of a <b>single EXE</b> to install your program for easy online distribution. Disk spanning is also supported.</li>
  <li>Standard Windows wizard interface.</li>
  <li><b>Customizable setup types</b>, e.g. Full, Minimal, Custom.</li>
  <li>Complete <b>uninstall</b> capabilities.</li>
  <li>Installation of files:<br />
Includes integrated support for "deflate", bzip2, and <b>7-Zip LZMA/LZMA2 file compression</b>. The installer has the ability to compare file version info, replace in-use files, use shared file counting, register DLL/OCX's and type libraries, and install fonts.</li>
  <li>Creation of shortcuts anywhere, including in the Start Menu and on the desktop.</li>
  <li>Creation of registry and .INI entries.</li>
  <li>Running other programs before, during or after install.</li>
  <li>Support for <b>multilingual</b> installs, including right-to-left language support.</li>
  <li>Support for passworded and encrypted installs.</li>
  <li>Support for <b>digitally signed</b> installs and uninstalls, including dual signing (SHA1 &amp; SHA256).</li>
  <li>Silent install and uninstall.</li>
  <li><b>Unicode</b> installs.
  </li><li>Integrated preprocessor option for advanced compile-time customization.</li>
  <li>Integrated Pascal scripting engine option for advanced run-time install and uninstall customization.</li>
  <li>Full <b>source code</b> is available from <a href="https://github.com/jrsoftware/issrc">GitHub</a>.</li>
  <li>Tiny footprint: only about 1.5 mB overhead with all features included.</li>
  <li>All features are fully <a href="ishelp.php">documented</a>.</li>
  <li>Used by <a href="https://code.visualstudio.com">Microsoft Visual Studio Code</a> and <a href="https://www.embarcadero.com/products/delphi">Embarcardero Delphi</a>.</li>
</ul>

<p><b><i>Is it really free of charge, even for commercial use?</i></b></p>

<p>Yes, it may be used completely free of charge, even when deploying commercial applications. However if you wish to show your appreciation and support its development you can <a href="/isdonate.php">make a donation</a>.</p>

